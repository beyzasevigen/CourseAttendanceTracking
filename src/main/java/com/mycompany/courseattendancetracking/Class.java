    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = table2.getSelectedRow();
        if (selectedRow != -1) { // Check if a row is actually selected
            int selectedId = (Integer)(tm.getValueAt(selectedRow, 0));
            for (int i = 0; i < Database.getClasss().size(); i++) {
                if (Database.getClasss().get(i).getId() == selectedId) {

                    Database.getUsers().remove(i);
                    break;
                }
            }
        }
        refreshTable();
    }//GEN-LAST:event_jButton2ActionPerformed

