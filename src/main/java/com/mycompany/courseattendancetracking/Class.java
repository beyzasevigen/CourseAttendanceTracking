    public void refreshTable(){
        for(ClassC classs: Database.getClasss()){
            tm.addRow(new Object[]{classs.getId(),
                classs.getClass()});
        }
    }
    
    public void tableInformation() {
        try {
            ClassC classs = new ClassC(Integer.parseInt(ıdTxt.getText()),
                                  classNameTxt.getText());
            Database.getClasss().add(classs);
            refreshTable();
        } catch (NumberFormatException e) {
            System.out.println("Invalid input for teacher ID.");
        }
    }
        
